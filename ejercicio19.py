# Escriba un algoritmo que solicite cuatro números y calcule la media aritmética (promedio) entre ellos.

# Constantes

PULGADA_A_CENTIMETRO = 2.54

# Entradas

cm = float(input('Ingrese cantidad de centímetros: '))

# Proceso

pulgadas = cm / PULGADA_A_CENTIMETRO

# Salidas

print(cm, 'centímetros son ', pulgadas, 'pulgadas')
