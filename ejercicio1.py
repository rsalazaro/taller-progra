#Ejercicio 1: Escriba un algoritmo que solicite cuatro números y calcule la media aritmética (promedio) entre ellos.

# Entradas

numero1 = float(input('Ingrese número 1: '))
numero2 = float(input('Ingrese número 2: '))
numero3 = float(input('Ingrese número 3: '))
numero4 = float(input('Ingrese número 4: '))

# Proceso

promedio = (numero1 + numero2 + numero3 + numero4)/4

# Salidas

print('El promedio de los 4 números es', promedio)
